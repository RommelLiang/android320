package com.tiemuyu.chuanchuan.activity.util;
/**
* 用于处理bitmap是用到的参数
* @Description: 用于处理bitmap是用到的参数
* @author hw
* @date 2014年8月18日 上午11:10:46 
* @Since version 1.0.0
 */
public class BitmapParam {
	
    private int desWidth; //目标宽度
    private int desHeight;//目标高度
    
	public int getDesWidth() {
		return desWidth;
	}
	public void setDesWidth(int desWidth) {
		this.desWidth = desWidth;
	}
	public int getDesHeight() {
		return desHeight;
	}
	public void setDesHeight(int desHeight) {
		this.desHeight = desHeight;
	}
    
}
